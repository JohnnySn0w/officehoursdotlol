<h1>Starting up an office hours</h1>
<h2>Tips and tricks from those of us dumb enough to try it</h2>

<ul>
  <li>pick a consistent time and place if you can</li>
  <li>have a "totem" item that you can use to identify your table so introverts don't have to ask around. "Is this the twitter table?" Mortifying. Most branches use a rubber duck, but you can use whatever you want.</li>
  <li>You can do weekly, bi-weekly, monthly, whatever you want. If you're not going to be weekly consider doing "first and third tuesdays" or whatever and not "every second"</li>
  <li>Submit a PR to this website's repo or ping <a href="https://www.twitter.com/whiskeytuesday">@whiskeytuesday</a> to get your office hours added</li>
</ul>
